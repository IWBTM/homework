#ipv4 的正则式匹配为(((\d{1,2})|(1\d{1,2})|(2[0-4]\d)|(25[0-5]))\.){3}((\d{1,2})|(1\d{1,2})|(2[0-4]\d)|(25[0-5]))
import re

if __name__=="__main__":
    ip="100"
    ip_re= r'(((\d{1,2})|(1\d{1,2})|(2[0-4]\d)|(25[0-5]))\.){3}((\d{1,2})|(1\d{1,2})|(2[0-4]\d)|(25[0-5]))'
    print("ip=",ip)
    if re.match(ip_re,ip):
        print("match")
    else:
        print("not match")